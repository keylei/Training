#ifndef CIRCLE_HPP
#define CIRCLE_HPP


#include <iostream>
#include <math.h>

#include "./Shape.hpp"
#include "../sdk/CustomException.hpp"
#include "../sdk/FormatConvertor.hpp"


namespace SSDK
{
/**
 *  @brief Circle
 *         表示圆形，继承shape基类
 *
 *  @author grace
 *  @version 1.00 2017-12-20 grace
 *                note:create it
 */
class Circle:public Shape
{
public:
    //>>>----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
    //constructor & destructor

    Circle();
    /**
     * @brief Circle 初始化圆的各种信息，包括半径圆心坐标等
     * @param radius 圆的半径
     * @param centerX 圆心的X坐标
     * @param centerY 圆心的Y坐标
     */
    Circle(double radius,double centerX,double centerY,ShapeType shapeType);
    virtual ~Circle();

    //<<<----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------


    //>>>----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
    //get & set function

    double radius() const {return m_radius;}
    void setRadius(double radius) {m_radius = radius;}

    //<<<----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------


    //>>>----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
    //member function
    /**
     * @brief calcArea 重写基类里的计算面积函数，
     *                 求圆的面积
     * @return 圆的面积
     */
    virtual double calcArea() const override;

    /**
     * @brief isContained 判断圆形是否包含参数里的形状（重写纯虚函数）
     *                    暂时不支持判断圆形包含其他形状，所以调用此函数直接跑出异常
     * @param pShape 被包含的形状
     * @return 有两种返回状态
     *         true:包含
     *         false:不包含
     */
    virtual bool contains(Shape *pShape) const override;

    //<<<----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------


private:
    //>>>----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
    //member variant

    double m_radius{0.0}; //圆的半径

    //<<<----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
};
}//End of namespace SSDK


#endif // CIRCLE_HPP
