#ifndef CIRCLE_HPP
#define CIRCLE_HPP

#include "CustomException.hpp"
#include "IAreaCalculatable.hpp"
#include "Shape.hpp"

namespace SSDK
{

/**
 *  @brief  表示圆形框，存有圆形的坐标位置和长宽
 *          具有计算矩形面积的功能
 *  @author lynn
 *  @version 1.00 2017-11-20 lynn
 *                note:create it
 */
class Circle:public Shape
{
public:

    //>>>----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
    //constructor & destructor

    /*
    *  @brief   默认构造函数
    *  @param   N/A
    *  @return  N/A
    */
    Circle();

    /*
    *  @brief   用给定位置信息、半径进行构造
    *  @param   radius:要设置的半径
    *           posX:要设置的x坐标
    *           posY:要设置的y坐标
    *  @return
    */
    Circle(double radius,double posX,double posY);

    /*
    *  @brief   默认析构函数
    *  @param   N/A
    *  @return  N/A
    */
    virtual ~Circle();

    //<<<----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------

    //>>>----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
    //set & get function

    double radius() {return this->m_radius;}
    void setRadius(double radius) {this->m_radius = radius;}

    //<<<----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------

    //>>>----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
    //member function

    /*
    *  @brief   计算圆形的面积
    *  @param   N/A
    *  @return  圆形的面积
    */
    virtual double calcArea();

    /*
    *  @brief   判断一个图形是否在圆形内
    *           支持的传入图形：
    *           占无
    *  @param   pShape :要判断的图形
    *  @return  true:在范围内
    *           false:不在范围内
    */
    virtual bool canContain(Shape *pShape) override;

    /*
    *  @brief   找出圆形的临界点
    *  @param   N/A
    *  @return  N/A
    */
    virtual void findBoundaryPoints() override;

    //<<<----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------

private:

    //>>>----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
    //member variant

    double m_radius{0};

    //<<<----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
};

}//End of namespace SSDK

#endif // CIRCLE_HPP
